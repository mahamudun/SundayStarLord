public interface Interface {
	
	// INterface can have only abstract methods,
	//Abstract method doesn;t have any implementation.
	// "i" is a convention for abstract method represent Interface.
	
	public abstract void iDoor();
		
	public abstract void iDoor(int doors);
	
	public abstract void iDoor(String glass);
	
	public abstract String iEngine();
	
	public abstract int iWheel();


	
}
